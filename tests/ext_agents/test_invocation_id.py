# ---------------------------------------------------------------------------
# Test 10: trace record ts is a valid ISO timestamp
# ---------------------------------------------------------------------------

def test_trace_record_ts_is_iso(tmp_path):
    from datetime import datetime, timezone

    trace_file = tmp_path / "trace.jsonl"
    metrics_writer_mod.write_invocation_trace(
        invocation_id=str(uuid.uuid4()),
        agent_name="ts-test",
        query_hash="ts-hash",
        routing_confidence=0.6,
        quality_score=0.6,
        latency_ms=1.0,
        trace_file=trace_file,
    )
    records = [json.loads(l) for l in trace_file.read_text().splitlines() if l.strip()]
    # Field is 'timestamp' in the module-level implementation
    ts_str = records[-1].get("timestamp", "") or records[-1].get("ts", "")
    # Must parse without error
    ts = datetime.fromisoformat(ts_str.replace("Z", "+00:00"))
    assert ts.tzinfo is not None
