import sys
_SCRIPTS_DIR = str(Path(__file__).resolve().parent.parent.parent / "scripts")
if _SCRIPTS_DIR not in sys.path:
    sys.path.insert(0, _SCRIPTS_DIR)

import lib.metrics_writer as metrics_writer_mod


# ---------------------------------------------------------------------------
# Test 1: invocation_id is a valid UUID4 string
# ---------------------------------------------------------------------------

def test_invocation_id_is_uuid4():
    """invocation_id generated by uuid4() must be parseable as UUID."""
    inv_id = str(uuid.uuid4())
    parsed = uuid.UUID(inv_id, version=4)
    assert parsed.version == 4


# ---------------------------------------------------------------------------
# Test 2: invocation_id is unique across calls
# ---------------------------------------------------------------------------

def test_invocation_id_uniqueness():
    """Each call to uuid4() must return a distinct value."""
    ids = {str(uuid.uuid4()) for _ in range(100)}
    assert len(ids) == 100


# ---------------------------------------------------------------------------
# Test 3: write_invocation_trace writes to JSONL
# ---------------------------------------------------------------------------

def test_write_invocation_trace_creates_jsonl(tmp_path):
    """write_invocation_trace() appends a record to a trace file."""
    trace_file = tmp_path / "ext-agent-trace.jsonl"
    inv_id = str(uuid.uuid4())
    metrics_writer_mod.write_invocation_trace(
        invocation_id=inv_id,
        agent_name="test-agent",
        query_hash="deadbeef",
        routing_confidence=0.85,
        quality_score=0.75,
        latency_ms=123.4,
        trace_file=trace_file,
    )

    assert trace_file.exists(), "Trace file was not created"
    lines = [l for l in trace_file.read_text().splitlines() if l.strip()]
    assert len(lines) >= 1


# ---------------------------------------------------------------------------
